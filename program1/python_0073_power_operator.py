'''

** -> power operator

'''

a = 5 ** 2  # 5 to the power of 2
print(a)

a = 5 ** 3  # 5 to the power of 3
print(a)

a = 5 ** 4  # 5 to the power of 4
print(a)

print("-" * 50)

a = 25 ** (1/2)     # 25 to the power of (1/2)      开平方
print(a)

a = 27 ** (1/3)     # 27 to the power of (1/3)   开立方
print(a)

a = 625 ** (1/4)     # 27 to the power of (1/4)   开4次方
print(a)


