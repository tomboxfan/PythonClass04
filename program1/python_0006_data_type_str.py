

# Step 1) Define 3 str variables

str1 = "Hello Python!"
str2 = 'str value surrounded by single quote'
str3 = "str value surrounded by double quotes"

'''
IMPORTANCE!!! ----------------------------------------------
you can use either single quote or double quotes in str
------------------------------------------------------------
'''

# step 2) I use the 3 str variables
print("variable str1 is: ", type(str1), ". str1 = ", str1)
print("variable str2 is: ", type(str2), ". str2 = ", str2)
print("variable str3 is: ", type(str3), ". str3 = ", str3)


first_name = 'Tom'
last_name = "Hanks"

full_name = first_name + " " + last_name # Tom Hanks

print("My favourite actor is: ", full_name)

'''
IMPORTANCE!!! ----------------------------------------------
'+' sign joins multiple str values and gives you a new str value
------------------------------------------------------------
'''


a = 1       # int variable
b = 2       # int variable
c = a + b   # int variable
print(c) # 3


d = '1'     # str variable
e = '2'     # str variable
f = d + e   # str variable
print(f)    # 12



'''
IMPORTANCE!!! ----------------------------------------------
'*' sign duplicates str and gives you a new str value
------------------------------------------------------------
'''

separate_line1 = '*' * 50
print(separate_line1)

separate_line2 = '*~' * 50
print(separate_line2)

separate_line3 = '*~ ' * 50
print(separate_line3)




