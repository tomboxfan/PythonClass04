animals = ['Tiger', 'Elephant', 'Snake', 'Shark']

'''
                 ['Tiger', 'Elephant', 'Snake', 'Shark']
positive index       0          1          2        3
negative index      -4         -3         -2       -1

'''

print("Access the list using positive index --------------------")

print(animals[0]) # Show me value at index 0, which is the first
print(animals[1]) # Show me value at index 1, which is the second
print(animals[2])
print(animals[3])


print("Access the list using negative index --------------------")
print(animals[-1])
print(animals[-2])
print(animals[-3])
print(animals[-4])
